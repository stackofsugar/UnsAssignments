// calculator.h
typedef unsigned int uint32_t;

uint32_t jumlah(uint32_t num1, uint32_t num2){
    return (num1 + num2);
}

uint32_t kurang(uint32_t num1, uint32_t num2){
    return (num1 - num2);
}

uint32_t kali(uint32_t num1, uint32_t num2){
    return (num1 * num2);
}

uint32_t bagi(uint32_t num1, uint32_t num2){
    return (num1 / num2);
}
